    private void btnRecordPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecordPaymentActionPerformed
        JTextField idField = new JTextField("PAY" + System.currentTimeMillis());
    idField.setEditable(false);
    
    String[] types = {"MEMBERSHIP_FEE", "SALARY", "OTHER"};
    JComboBox<String> typeCombo = new JComboBox<>(types);
    
    JTextField amountField = new JTextField();
    JTextField descField = new JTextField();
    JTextField refField = new JTextField();
    
    Object[] message = {
        "Payment ID:", idField,
        "Type:", typeCombo,
        "Amount:", amountField,
        "Description:", descField,
        "Reference ID:", refField
    };
    
    int option = JOptionPane.showConfirmDialog(
        this, 
        message, 
        "Record Payment", 
        JOptionPane.OK_CANCEL_OPTION
    );
    
    if (option == JOptionPane.OK_OPTION) {
        try {
            Payment payment = new Payment();
            payment.setPaymentId(idField.getText());
            payment.setPaymentType((String) typeCombo.getSelectedItem());
            payment.setAmount(Double.parseDouble(amountField.getText()));
            payment.setDate(new Date());
            payment.setDescription(descField.getText());
            payment.setReferenceId(refField.getText());
            
            financeService.recordPayment(payment);
            loadPayments();
            updateTotals();
            
            JOptionPane.showMessageDialog(this, "Payment recorded successfully!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid amount format!");
        }
    }
    }//GEN-LAST:event_btnRecordPaymentActionPerformed

    private void btnProcessSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessSalaryActionPerformed
        // Get all staff
    StaffService staffService = new StaffService();
    ArrayList<Staff> staffList = staffService.getStaffDetails();
    
    if (staffList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No staff found!");
        return;
    }
    
    // Create staff dropdown
    String[] staffNames = new String[staffList.size()];
    for (int i = 0; i < staffList.size(); i++) {
        staffNames[i] = staffList.get(i).getStaffId() + " - " + staffList.get(i).getName();
    }
    
    JComboBox<String> staffCombo = new JComboBox<>(staffNames);
    JTextField amountField = new JTextField();
    
    Object[] message = {
        "Select Staff:", staffCombo,
        "Salary Amount:", amountField
    };
    
    int option = JOptionPane.showConfirmDialog(
        this, 
        message, 
        "Process Salary", 
        JOptionPane.OK_CANCEL_OPTION
    );
    
    if (option == JOptionPane.OK_OPTION) {
        try {
            int selectedIndex = staffCombo.getSelectedIndex();
            Staff selectedStaff = staffList.get(selectedIndex);
            double amount = Double.parseDouble(amountField.getText());
            
            financeService.processSalary(selectedStaff, amount);
            loadPayments();
            updateTotals();
            
            JOptionPane.showMessageDialog(this, "Salary processed successfully!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid amount format!");
        }
    }
    }//GEN-LAST:event_btnProcessSalaryActionPerformed

