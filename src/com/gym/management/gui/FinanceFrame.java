    private void loadPayments() {
    tableModel.setRowCount(0);
    
    ArrayList<Payment> payments = financeService.getPaymentDetails();
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    
    for (Payment payment : payments) {
        Object[] row = {
            payment.getPaymentId(),
            payment.getPaymentType(),
            payment.getAmount(), 
            dateFormat.format(payment.getDate()),
            payment.getDescription()
        };
        tableModel.addRow(row);
    }
}

private void updateTotals() {
    double revenue = financeService.getTotalRevenue();
    double expenses = financeService.getTotalOutcome();
    
    lblTotalRevenue.setText("Total Revenue: $" + String.format("%.2f", revenue));
    lblTotalExpenses.setText("Total Expenses: $" + String.format("%.2f", expenses));
}

