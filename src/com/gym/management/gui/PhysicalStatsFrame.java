            result += "<table style='margin:auto; text-align:left;'>";
            result += "<tr><td><b>Underweight:</b></td><td>&lt; 18.5</td></tr>";
            result += "<tr><td><b>Healthy Weight:</b></td><td>18.5 - 24.9</td></tr>";
            result += "<tr><td><b>Overweight:</b></td><td>25.0 - 29.9</td></tr>";
            result += "<tr><td><b>Obese:</b></td><td>â‰¥ 30.0</td></tr>";
            result += "</table>";
