    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JTextField memberIdField = new JTextField();
    JTextField nameField = new JTextField();
    JTextField weightField = new JTextField();
    JTextField heightField = new JTextField();
    JTextArea descArea = new JTextArea(3, 20);
    descArea.setLineWrap(true);
    
    Object[] message = {
        "Member ID:", memberIdField,
        "Name:", nameField,
        "Weight (kg):", weightField,
        "Height (m):", heightField,
        "Description:", new javax.swing.JScrollPane(descArea)
    };
    
    int option = JOptionPane.showConfirmDialog(
        this, 
        message, 
        "Add Physical Stats", 
        JOptionPane.OK_CANCEL_OPTION
    );
    
    if (option == JOptionPane.OK_OPTION) {
        try {
            String memberId = memberIdField.getText();
            String name = nameField.getText();
            double weight = Double.parseDouble(weightField.getText());
            double height = Double.parseDouble(heightField.getText());
            String description = descArea.getText();
            
            // Calculate BMI
            double bmi = weight / (height * height);
            String status = getBMIStatus(bmi);
            
            tableModel.addRow(new Object[]{
                memberId,
                name,
                String.format("%.1f", weight),
                String.format("%.2f", height),
                String.format("%.2f", bmi),
                status,
                description
            });
            
            // Show BMI result
            String result = "Physical stats added successfully!\n\n";
            result += "BMI: " + String.format("%.2f", bmi) + "\n";
            result += "Status: " + status;
            
            JOptionPane.showMessageDialog(this, result);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format!");
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = tblPhysicalStats.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a member to update stats!");
        return;
    }
    
    String memberId = (String) tableModel.getValueAt(selectedRow, 0);
    String name = (String) tableModel.getValueAt(selectedRow, 1);
    String currentWeight = (String) tableModel.getValueAt(selectedRow, 2);
    String currentHeight = (String) tableModel.getValueAt(selectedRow, 3);
    String currentDesc = (String) tableModel.getValueAt(selectedRow, 6);
    
    JTextField weightField = new JTextField(currentWeight);
    JTextField heightField = new JTextField(currentHeight);
    JTextArea descArea = new JTextArea(currentDesc, 3, 20);
    descArea.setLineWrap(true);
    
    Object[] message = {
        "Member: " + name,
        "Weight (kg):", weightField,
        "Height (m):", heightField,
        "Description:", new javax.swing.JScrollPane(descArea)
    };
    
    int option = JOptionPane.showConfirmDialog(
        this, 
        message, 
        "Update Physical Stats", 
        JOptionPane.OK_CANCEL_OPTION
    );
    
    if (option == JOptionPane.OK_OPTION) {
        try {
            double weight = Double.parseDouble(weightField.getText());
            double height = Double.parseDouble(heightField.getText());
            String description = descArea.getText();
            
            // Calculate new BMI
            double bmi = weight / (height * height);
            String status = getBMIStatus(bmi);
            
            tableModel.setValueAt(String.format("%.1f", weight), selectedRow, 2);
            tableModel.setValueAt(String.format("%.2f", height), selectedRow, 3);
            tableModel.setValueAt(String.format("%.2f", bmi), selectedRow, 4);
            tableModel.setValueAt(status, selectedRow, 5);
            tableModel.setValueAt(description, selectedRow, 6);
            
            String result = "Physical stats updated successfully!\n\n";
            result += "New BMI: " + String.format("%.2f", bmi) + "\n";
            result += "Status: " + status;
            
            JOptionPane.showMessageDialog(this, result);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid number format!");
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

